export function t(key: string, lang: string = 'en'): string {
  const keys = key.split('.');
  let value: any = translations[lang as keyof typeof translations] || translations.en;
  
  for (const k of keys) {
    value = value?.[k];
    if (!value) break;
  }
  
  return value || key;
}
